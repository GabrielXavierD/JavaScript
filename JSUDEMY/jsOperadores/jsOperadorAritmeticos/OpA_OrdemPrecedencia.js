/**
 Operadores aritméticos em JavaScript e suas precedências de execução:
(segue os principios da matematica)

 Precedência do maior para o menor (da esquerda p/ direita):
  -Parenteses: aplica precedencia, faz o que estiver dentro ser executado primeiro. Ex.: (a+b), g/(a+(b+(d+e)+c)+) etc...
 
 1. Exponenciação: **
 2. Multiplicação: *
 3. Divisão: /
 4. Resto da divisão (Módulo): %
 5. Adição: +
 6. Subtração: -
 7. Incremento: ++ (quando usado em forma de prefixo)
 8. Decremento: -- (quando usado em forma de prefixo)
 
 Nota: Incremento (++) e Decremento (--) têm precedência diferente 
 quando usados como prefixo (++x ou --x) e sufixo (x++ ou x--).
 Como sufixo, eles são executados após a expressão onde estão, 
 mas como prefixo, são executados antes da expressão.
 */