function playAudio() {
    let variavel = document.getElementById("meuAudio"); //acessando elemento com id = "meuAudio"
    variavel.play(); //dando play na variavel
}

function pauseAudio() {
    let variavel = document.getElementById("meuAudio"); //acessando elemento com id = "meuAudio"
    variavel.pause(); //dando pause na variavel
}